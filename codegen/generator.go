func (g *Generator) generateGraphQLClient() error {
	// Look for schema files
	schemaDir := "graphql"
	if _, err := os.Stat(schemaDir); os.IsNotExist(err) {
		return nil // No graphql directory, skip
	}

	var schemaSb strings.Builder
	err := filepath.Walk(schemaDir, func(path string, info os.FileInfo, err error) error {
		if err != nil || info.IsDir() || !strings.HasSuffix(path, ".graphql") {
			return nil
		}
		content, err := os.ReadFile(path)
		if err == nil {
			schemaSb.Write(content)
			schemaSb.WriteString("\n")
		}
		return nil
	})
	if err != nil {
		return err
	}

	if schemaSb.Len() == 0 {
		return nil
	}

	// Basic GraphQL client generation
	content := `// Auto-generated by Astra. DO NOT EDIT.
import { GraphQLClient } from 'graphql-request';
import { useQuery, useMutation } from '@tanstack/react-query';

const endpoint = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:8080';
const gqlClient = new GraphQLClient(endpoint + '/graphql', {
  headers: () => {
    const token = typeof window !== 'undefined' ? localStorage.getItem('astra_access_token') : null;
    return token ? { Authorization: ` + "`" + `Bearer ${token}` + "`" + ` } : {};
  }
});

export const gql = gqlClient;

// Add high-level helpers here...
`
	return os.WriteFile(filepath.Join(g.OutputDir, "graphql.ts"), []byte(content), 0644)
}

